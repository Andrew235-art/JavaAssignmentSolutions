// 2. Area of a Circle 

public class AreaOfCircle { 
    public static void main(String[] args) { 
        double radius = 5.0; // Example radius double 
        area = Math.PI * radius * radius; 
        int areaAsInt = (int) area; // Type casting to integer 
        System.out.println("Area as integer: " + areaAsInt); 
    } 
}
